//Function to remove duplicates from sorted linked list.
Node *removeDuplicates(Node *head)
{
    Node* node1 = head;
    Node* node2 = head->next;
    while(node2){
        if(node1->data == node2->data){
            node2 = node2->next;
            continue;
        }
        node1->next = node2;
        node1 = node2;
        node2 = node2->next;
    }
    node1->next = NULL;
    return head;
    
}